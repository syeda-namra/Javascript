let names=[`Namra`, `Anum`, `Ayisha`, `Safiya`];        //  array starts from zero


console.log(`Without using Loop`);

console.log(names[0],`\n`);
console.log(names[1],`\n`);
console.log(names[2],`\n`);
console.log(names[3],`\n`);

console.log(`Using Loop`)

for (i=0; i<names.length;i++) //length returns the length of an array starting
{
    console.log(names[i],`\n`);
}